export const GLOSSARY_TERMS = Object.keys(GLOSSARY).sort((a, b) => b.length - a.length);

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

export function buildGlossaryRegex(): RegExp {
  return new RegExp(`(${GLOSSARY_TERMS.map(escapeRegExp).join("|")})`, "gi");
}

export function getGlossaryTip(term: string): string | undefined {
  const exact = GLOSSARY[term];
  if (exact) return exact;
  const found = GLOSSARY_TERMS.find((key) => key.toLowerCase() === term.toLowerCase());
  return found ? GLOSSARY[found] : undefined;
}
